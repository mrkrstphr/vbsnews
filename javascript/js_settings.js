///////////////////////////////////////////////////////////////////////////////////////////////////
// FUNCTION - launchhelp() : opens a new window with a help item
///////////////////////////////////////////////////////////////////////////////////////////////////
function launchhelp(item) {
    var s = 'toolbar=0,scrollbars=1,location=0,statusbar=0,menubar=0,resizable=0,';
    s += 'width=250,height=140,left=401,top=207';

    var win = window.open('help.php?item=' + item, 'vbsNewsHelp', s);
}

///////////////////////////////////////////////////////////////////////////////////////////////////
// FUNCTION - enabler() : enables or disables a form element
///////////////////////////////////////////////////////////////////////////////////////////////////
function enabler(element, en) {
    if (en == 1) {
        element.disabled = false;
    } else {
        element.disabled = true;
    }
}